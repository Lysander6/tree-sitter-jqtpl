static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  eof = lexer->eof(lexer);
  switch (state) {
    case 0:
      if (eof) ADVANCE(33);
      if (lookahead == '(') ADVANCE(44);
      if (lookahead == ')') ADVANCE(48);
      if (lookahead == ',') ADVANCE(46);
      if (lookahead == '{') ADVANCE(23);
      if (lookahead == '}') ADVANCE(25);
      END_STATE();
    case 1:
      if (lookahead == '!') ADVANCE(39);
      if (lookahead == '/') ADVANCE(10);
      if (lookahead == '=') ADVANCE(36);
      if (lookahead == 'e') ADVANCE(3);
      if (lookahead == 'h') ADVANCE(21);
      if (lookahead == 'i') ADVANCE(11);
      if (lookahead == 'p') ADVANCE(4);
      END_STATE();
    case 2:
      if (lookahead == ')') ADVANCE(51);
      if (lookahead != 0 &&
          lookahead != '(') ADVANCE(2);
      END_STATE();
    case 3:
      if (lookahead == 'a') ADVANCE(7);
      if (lookahead == 'l') ADVANCE(20);
      END_STATE();
    case 4:
      if (lookahead == 'a') ADVANCE(19);
      END_STATE();
    case 5:
      if (lookahead == 'a') ADVANCE(17);
      END_STATE();
    case 6:
      if (lookahead == 'a') ADVANCE(8);
      END_STATE();
    case 7:
      if (lookahead == 'c') ADVANCE(13);
      END_STATE();
    case 8:
      if (lookahead == 'c') ADVANCE(14);
      END_STATE();
    case 9:
      if (lookahead == 'e') ADVANCE(42);
      END_STATE();
    case 10:
      if (lookahead == 'e') ADVANCE(6);
      if (lookahead == 'i') ADVANCE(12);
      END_STATE();
    case 11:
      if (lookahead == 'f') ADVANCE(40);
      END_STATE();
    case 12:
      if (lookahead == 'f') ADVANCE(28);
      END_STATE();
    case 13:
      if (lookahead == 'h') ADVANCE(43);
      END_STATE();
    case 14:
      if (lookahead == 'h') ADVANCE(29);
      END_STATE();
    case 15:
      if (lookahead == 'i') ADVANCE(5);
      END_STATE();
    case 16:
      if (lookahead == 'l') ADVANCE(38);
      END_STATE();
    case 17:
      if (lookahead == 'l') ADVANCE(50);
      END_STATE();
    case 18:
      if (lookahead == 'm') ADVANCE(16);
      END_STATE();
    case 19:
      if (lookahead == 'r') ADVANCE(22);
      END_STATE();
    case 20:
      if (lookahead == 's') ADVANCE(9);
      END_STATE();
    case 21:
      if (lookahead == 't') ADVANCE(18);
      END_STATE();
    case 22:
      if (lookahead == 't') ADVANCE(15);
      END_STATE();
    case 23:
      if (lookahead == '{') ADVANCE(1);
      END_STATE();
    case 24:
      if (lookahead == '}') ADVANCE(25);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(34);
      END_STATE();
    case 25:
      if (lookahead == '}') ADVANCE(37);
      END_STATE();
    case 26:
      if (lookahead == '}') ADVANCE(41);
      END_STATE();
    case 27:
      if (lookahead == '}') ADVANCE(49);
      END_STATE();
    case 28:
      if (lookahead == '}') ADVANCE(26);
      END_STATE();
    case 29:
      if (lookahead == '}') ADVANCE(27);
      END_STATE();
    case 30:
      if (lookahead != 0 &&
          lookahead != ')') ADVANCE(47);
      END_STATE();
    case 31:
      if (lookahead != 0 &&
          lookahead != ',') ADVANCE(45);
      END_STATE();
    case 32:
      if (eof) ADVANCE(33);
      if (lookahead == '{') ADVANCE(23);
      if (lookahead != 0) ADVANCE(35);
      END_STATE();
    case 33:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 34:
      ACCEPT_TOKEN(sym_code);
      if (lookahead != 0 &&
          lookahead != '\n' &&
          lookahead != '}') ADVANCE(34);
      END_STATE();
    case 35:
      ACCEPT_TOKEN(sym_content);
      if (lookahead != 0 &&
          lookahead != '{') ADVANCE(35);
      END_STATE();
    case 36:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACE_EQ);
      END_STATE();
    case 37:
      ACCEPT_TOKEN(anon_sym_RBRACE_RBRACE);
      END_STATE();
    case 38:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACEhtml);
      END_STATE();
    case 39:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACE_BANG);
      END_STATE();
    case 40:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACEif);
      END_STATE();
    case 41:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACE_SLASHif_RBRACE_RBRACE);
      END_STATE();
    case 42:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACEelse);
      END_STATE();
    case 43:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACEeach);
      END_STATE();
    case 44:
      ACCEPT_TOKEN(anon_sym_LPAREN);
      END_STATE();
    case 45:
      ACCEPT_TOKEN(aux_sym_each_directive_token1);
      if (lookahead != 0 &&
          lookahead != ',') ADVANCE(45);
      END_STATE();
    case 46:
      ACCEPT_TOKEN(anon_sym_COMMA);
      END_STATE();
    case 47:
      ACCEPT_TOKEN(aux_sym_each_directive_token2);
      if (lookahead != 0 &&
          lookahead != ')') ADVANCE(47);
      END_STATE();
    case 48:
      ACCEPT_TOKEN(anon_sym_RPAREN);
      END_STATE();
    case 49:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACE_SLASHeach_RBRACE_RBRACE);
      END_STATE();
    case 50:
      ACCEPT_TOKEN(anon_sym_LBRACE_LBRACEpartial);
      END_STATE();
    case 51:
      ACCEPT_TOKEN(aux_sym_partial_directive_token1);
      if (lookahead == '(') ADVANCE(2);
      if (lookahead != 0 &&
          lookahead != ')') ADVANCE(51);
      END_STATE();
    default:
      return false;
  }
}

